/**
 * Processes the function "greet".
 * @param name - parameter
 * @returns string
 */
function greet(name: string) {
  return `Hello, ${name}`;
}
/**
 * Processes the function "add".
 * @param a - parameter
 * @param b - parameter
 * @returns number
 */
const add = (a: number, b: number) => a + b;
